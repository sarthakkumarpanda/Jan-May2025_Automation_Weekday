package com.learning_java_Day8_25th_Feb_2025_Basics_of_Java;

public class Topic3_main_method_in_java {
	
	//control will remain at one place
	//main() method is the execution engine of a java program
	//main() method may or may not have logic but it is still the center of execution
	//nomenclature of main() method cannot change too much
	//args is nothing but arguments

	
	public static void main(String[] args) {
		addition();	

	}
	
	public static void addition() {
		int i = 1;
		int j = 2;
		System.out.println(i + j);
	}
	

}
