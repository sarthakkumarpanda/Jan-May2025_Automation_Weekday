package com.learning_java_Day21_9th_Apr_2025_Wrapper_Class;

public class Topic2_What_is_Wrapper_Class {
	
	//Wrapper Classes are pre-defined classes which can contain primitive data type
	//They wrap primitive data types to Wrapper Class Object
	
	//int - Integer
	//long - Long
	//short - Short
	//boolean - Boolean
	//char - Character
	//double - Double
	//float - Float
	//byte - Byte

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("The primitive data type value is : " + a);
		
		
		Integer b = 10;
		System.out.println(b);
		

	}

}
